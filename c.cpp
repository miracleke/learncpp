#include<iostream>
using namespace std;
int main() {
    int n, c = 1;
    cin >> n;
    if(n <= 100000) 
    { 
	    c = n * 0.1;
    }
    if(n <= 200000 && n > 100000)
    { 
	    c = 100000 * 0.1 + (n - 100000) * 0.075;
    }
    if(n <= 400000 && n > 200000)  
    {
        c = 100000 * 0.1 + 100000 * 0.075 + (n - 200000) * 0.05;
    }     
    if(n <= 600000 && n > 400000)
	    c=100000*0.1+100000*0.075+200000*0.05+(n-400000)*0.03;	    
    if(n <= 1000000 && n > 600000)
            c=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(n-600000)*0.015;
    if(n > 1000000)
            c=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(n-1000000)*0.001;
    
    cout << c << endl;
    
    return 0;
}
